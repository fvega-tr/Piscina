#include <unistd.h>

int	main(int agrc, char **agrv)
{
	int i;
	int j;

	i = agrc - 1;
	j = 0;
	while (0 < i)
	{
		j = 0;
		while (agrv[i][j] != '\0')
		{
			write(1, &agrv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i--;
	}
}
